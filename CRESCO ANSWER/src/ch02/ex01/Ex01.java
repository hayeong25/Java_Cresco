package ch02.ex01;

public class Ex01 {
	public static void main(String[] args) {
		int a = 1 + 7;
		int b = 12 - 7;
		int c = 12 * 7;
		int d = 56 / 7;
		double e = (double) 60 / 7;
		System.out.println("1+7=" + a + "です。");
		System.out.println("12-7=" + b + "です。");
		System.out.println("12*7=" + c + "です。");
		System.out.println("56/7=" + d + "です。");
		System.out.println("60/7=" + e + "です。");
	}

}
